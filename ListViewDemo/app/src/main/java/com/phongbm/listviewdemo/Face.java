package com.phongbm.listviewdemo;

public class Face {
    private String name;
    private int imageId;

    public Face(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}